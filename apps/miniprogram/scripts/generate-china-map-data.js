const fs = require('fs');
const path = require('path');

const SOURCE_URL = 'https://geojson.cn/api/china/100000.json';
const OUTPUT_PATH = path.resolve(__dirname, '../data/china-provinces.ts');
const EPSILON = 0.035;
const PRECISION = 3;

main().catch((error) => {
  console.error(error);
  process.exit(1);
});

async function main() {
  const response = await fetch(SOURCE_URL);
  if (!response.ok) {
    throw new Error(`Failed to fetch China map data: ${response.status} ${response.statusText}`);
  }

  const source = await response.json();
  const features = source.features.map(toCompactFeature);
  const output = [
    'import type { ChinaMapFeature } from "../utils/map-geometry";',
    '',
    '// Generated by scripts/generate-china-map-data.js from https://geojson.cn/api/china/100000.json.',
    '// Simplified for the MVP ink-map canvas renderer; verify map compliance before public production release.',
    `export const chinaProvinces: ChinaMapFeature[] = ${JSON.stringify(features)};`,
    ''
  ].join('\n');

  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
  console.log(`Wrote ${features.length} map features to ${path.relative(process.cwd(), OUTPUT_PATH)}`);
}

function toCompactFeature(feature) {
  const properties = feature.properties || {};
  const compact = {
    name: properties.name || properties.fullname || '',
    code: String(properties.code || ''),
    center: Array.isArray(properties.center) ? roundPoint(properties.center) : undefined
  };

  if (feature.geometry.type === 'Polygon') {
    compact.rings = simplifyPolygon(feature.geometry.coordinates);
  } else if (feature.geometry.type === 'MultiPolygon') {
    compact.rings = feature.geometry.coordinates.flatMap(simplifyPolygon);
  } else if (feature.geometry.type === 'MultiLineString') {
    compact.lines = feature.geometry.coordinates.map(simplifyLine);
  } else {
    throw new Error(`Unsupported geometry type: ${feature.geometry.type}`);
  }

  return compact;
}

function simplifyPolygon(polygon) {
  return polygon.map(simplifyLine).filter((ring) => ring.length >= 2);
}

function simplifyLine(points) {
  if (points.length <= 3) {
    return points.map(roundPoint);
  }

  const openPoints = samePoint(points[0], points[points.length - 1]) ? points.slice(0, -1) : points;
  const simplified = douglasPeucker(openPoints, EPSILON).map(roundPoint);
  if (samePoint(points[0], points[points.length - 1]) && !samePoint(simplified[0], simplified[simplified.length - 1])) {
    simplified.push(simplified[0]);
  }
  return simplified;
}

function douglasPeucker(points, epsilon) {
  if (points.length <= 2) {
    return points;
  }

  let maxDistance = 0;
  let index = 0;
  for (let i = 1; i < points.length - 1; i += 1) {
    const distance = perpendicularDistance(points[i], points[0], points[points.length - 1]);
    if (distance > maxDistance) {
      index = i;
      maxDistance = distance;
    }
  }

  if (maxDistance <= epsilon) {
    return [points[0], points[points.length - 1]];
  }

  const left = douglasPeucker(points.slice(0, index + 1), epsilon);
  const right = douglasPeucker(points.slice(index), epsilon);
  return left.slice(0, -1).concat(right);
}

function perpendicularDistance(point, start, end) {
  const dx = end[0] - start[0];
  const dy = end[1] - start[1];
  if (dx === 0 && dy === 0) {
    return distance(point, start);
  }
  return Math.abs(dy * point[0] - dx * point[1] + end[0] * start[1] - end[1] * start[0]) / Math.sqrt(dx * dx + dy * dy);
}

function distance(a, b) {
  const dx = a[0] - b[0];
  const dy = a[1] - b[1];
  return Math.sqrt(dx * dx + dy * dy);
}

function roundPoint(point) {
  return [round(point[0]), round(point[1])];
}

function round(value) {
  return Number(value.toFixed(PRECISION));
}

function samePoint(a, b) {
  return a && b && a[0] === b[0] && a[1] === b[1];
}
